class Main {
	public static void main(String[] args) {
		// ++ = incremento
		// -- = decremento
		for (int cont = 0; cont < 10; cont++) {

			System.out.println("Valor do contador: " + cont);

		}
		;

		for (int cont2 = 5; cont2 <= 20; cont2++) {
			System.out.println("Valor do contador de 5 a 20: " + cont2);
		}
	}

}